#include "push_swap.h"

long	ft_atol(const char *nptr)
{
	int		sign;
	int		i;
	long	result;

	i = 0;
	result = 0;
	sign = 1;
	while (nptr[i] >= 9 && nptr[i] <= 13 || nptr[i] == 32)
		i++;
	if (nptr[i] == '+' || nptr[i] == '-')
	{
		if (nptr[i] == '-')
			sign *= -1;
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		result = (result * 10) + (nptr[i] - 48);
		i++;
	}
	return (result * sign);
}
